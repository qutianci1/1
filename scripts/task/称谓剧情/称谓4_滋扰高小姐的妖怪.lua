function 任务:任务NPC对话(玩家, NPC)
    if NPC.名称 == '高老先生' and NPC.台词 then
        NPC.头像 = NPC.外形
        if self.进度 == 0 then
            self.对话进度 = 0
            NPC.台词 = _台词[1]
            NPC.结束 = false
        elseif self.进度==3 then
            NPC.台词 = _台词[10]
            self:完成(玩家)
             玩家:刷新追踪面板()
        end
    elseif NPC.名称 == '高小姐' and NPC.台词 then
        NPC.头像 = NPC.外形
        if self.进度 == 1 then
            self.对话进度 = 0
            NPC.台词 = _台词[5]
            NPC.结束 = false
        end
    elseif NPC.名称 == '猪八戒' and NPC.台词 then
        if self.进度 == 2 then
            self.对话进度 = 0
            NPC.头像 = 玩家.原形
            NPC.台词 = _台词[7]
            NPC.结束 = false
        end
    end
end

function 任务:任务NPC菜单(玩家, NPC, i)
    if NPC.名称 == '高老先生' then
        if self.进度 == 0 then
            if self.对话进度 == 0 then
                self.对话进度 = 1
                NPC.头像 = 玩家.原形
                NPC.台词 = _台词[2]
                NPC.结束 = false
            elseif self.对话进度 == 1 then
                self.对话进度 = 2
                NPC.头像 = NPC.外形
                NPC.台词 = _台词[3]
                NPC.结束 = false
            elseif self.对话进度 == 2 then
                NPC.头像 = 玩家.原形
                NPC.台词 = _台词[4]
                NPC.结束 = nil
                self.进度 = 1
                 玩家:刷新追踪面板()
            end

        end
    elseif NPC.名称 == '高小姐' then
        if self.进度 == 1 then
            if self.对话进度 == 0 then
                NPC.头像 = 玩家.原形
                NPC.台词 = _台词[6]
                NPC.结束 = nil
                self.进度 = 2
                 玩家:刷新追踪面板()
            end
        end
    elseif NPC.名称 == '猪八戒' then
        if self.进度 == 2 then
            if self.对话进度 == 0 then
                self.对话进度 = 1
                NPC.头像 = NPC.外形
                NPC.台词 = _台词[8]
                NPC.结束 = false
            elseif self.对话进度 == 1 then
                NPC.结束 = nil
                self:任务攻击事件(玩家, NPC)
            end
        end
    end
end

function 任务:完成(玩家)
    玩家:添加声望(150)
    玩家:添加银子(5000)
    玩家:添加物品({生成物品 {名称 = '清风白雪', 数量 = 1}})
    玩家:提示窗口('#Y因为你的胆大心细，你在这个世界的声望得到提升。获得150点声望，5000两银子和一个清风白雪。')

    local r = 玩家:取任务('引导_升级检测')
    if r then
        r:四称完成检测(玩家, '八戒')
    end
    self:删除()
end

