function 任务:任务攻击事件(玩家, NPC)
    if NPC.名称 == '猪八戒' then
        if self.进度 == 2 then
            local r = 玩家:进入战斗('scripts/task/称谓剧情/称谓4_滋扰高小姐的妖怪.lua', self)
            if r then
                self.进度 = 3
                玩家:添加银子(65214)
                玩家:添加经验(454218)
                玩家:常规提示('#R你打败了猪八戒，获得了454218经验和65214两银子！！')
                 玩家:刷新追踪面板()
            end
        end
    end
end

