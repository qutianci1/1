function 任务:寻人检查(玩家)
    for _, v in pairs(self.寻人) do
        if v == 0 then
            return
        end
    end
    self.进度 = 4
end
function 任务:完成(玩家)
    玩家:添加声望(50)
    玩家:常规提示('#Y你帮助玄奘我到佛衣宝物，你的声望增加了50点。')
    local r = 玩家:取任务('引导_升级检测')
    if r then
        r:十一称完成检测(玩家, '宝物')
    end
    self:删除()
end

function 任务:任务NPC给予(玩家, NPC, cash, items)
    if NPC.名称 == '玄奘' then
        if self.进度 == 8 then
            if items[1] and (items[1].名称 == '锦斓袈裟' or items[1].名称 == '九环锡杖') then --
                if self.寻物[items[1].名称] == 0 then
                    if items[1].数量 >= 1 then
                        items[1]:接受(1)
                        self.寻物[items[1].名称] = 1
                        if self.寻物.锦斓袈裟 == 1 and self.寻物.九环锡杖 == 1 then
                            NPC.台词 = _台词[35].台词
                            NPC.头像 = NPC.外形
                            NPC.结束 = false
                        end
                    end
                end
            end
        end
    end
end

