function 任务:任务NPC菜单(玩家, NPC, i)
    if NPC.名称 == '李世民' then
        if self.进度 == 0 then
            self.对话进度 = 2
            NPC.台词 = _台词[2].台词
            NPC.头像 = 玩家.原形
            NPC.结束 = nil
            self.进度 = 1
            玩家:添加经验(43230)
        end
    elseif NPC.名称 == '玄奘' then
        if self.进度 == 1 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 9 then
                self.进度 = 2
            end
        elseif self.进度 == 8 then
            NPC.台词 = _台词[36].台词
            NPC.头像 = 玩家.原形
            NPC.结束 = nil
            self:完成(玩家)
        end
    elseif NPC.名称 == '玉皇大帝' then
        if self.进度 == 2 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 16 then
                self.进度 = 3
            end
        elseif self.进度 == 4 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
