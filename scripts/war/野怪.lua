                local dj = v.对象.接口:取当前地图等级()
                v.对象.接口:添加银子(math.floor(dj * 13))
                v.对象.接口:添加任务经验(math.floor(dj * 29))
                local r = v.对象.接口:取物品是否存在("元气蛋")
                if r then
                    r:添加血气(v.对象.接口)
                end
                --坐骑相关
                local z = v.对象.接口:取乘骑坐骑()
                if z then
                    z:添加经验(2)
                    z:添加熟练(2)
                end
                local r = v.对象.接口:取任务('日常_帮派任务')
                if r then
                    if r.分类 == 9 then
                        if (r.位置 == '青灵草' and v.对象.接口:取当前地图().名称 == '傲来国') or (r.位置 == '血色茶花' and (v.对象.接口:取当前地图().名称 == '地狱迷宫一层' or v.对象.接口:取当前地图().名称 == '地狱迷宫二层' or v.对象.接口:取当前地图().名称 == '地狱迷宫三层' or v.对象.接口:取当前地图().名称 == '地狱迷宫四层')) or (r.位置 == '千年寒冰' and v.对象.接口:取当前地图().名称 == '北俱芦洲') then
