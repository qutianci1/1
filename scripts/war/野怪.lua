local function _取怪物属性(玩家, id)
    local list = {}
    local t = _野怪[id][math.random(#_野怪[id])]
    for k, v in pairs(t) do
        list[k] = v
    end
    return list
end

local function _取怪物属性平均值(玩家, id)
    local list = {血初值 = 0, 法初值 = 0, 攻初值 = 0, 敏初值 = 0, 成长 = 0, 技能 = {}, 等级 = 0}
    local t = _野怪[id]
    for i=1,#t do
        for k,v in pairs(list) do
            if k == '技能' then
                if #t[i].技能 >= 1 then
                    for n=1,#t[i].技能 do
                        table.insert(v, t[i].技能[n])
                    end
                end
            else
                list[k] = list[k] + t[i][k]
            end
        end
    end
    for k,v in pairs(list) do
        if k ~= '技能' then
            if k == '成长' then
                list[k] = list[k] / #t * 1.5
            elseif k == '等级' then
                list[k] = math.floor(list[k] / #t + 20)
            else
                list[k] = math.floor(list[k] / #t * 10)
            end
        end
    end
    return list
end

