function 战斗:战斗初始化(玩家, id)
    if _野怪[id] then
        local n = 1
        if not 玩家.是否组队 then
            n = math.random(2, 3)
        else
            n = 玩家:取队伍人数()
            n = math.random(n + 1, n * 2 + 1)
        end
        if n > 10 then
            n = 10
        end
        --练功区boss
        if _boss[id] and 玩家.是否组队 and math.random(1, 100) <= 20 then
