                    t.外形 = _boss[id].外形
                    self:加入敌方(i, 生成战斗怪物(生成怪物属性( t, '困难' )))
                else
                    local t = _取怪物属性(玩家, id)
                    t.血初值 = t.血初值 * 2
                    t.法初值 = t.法初值 * 2
                    t.攻初值 = t.攻初值 * 2
