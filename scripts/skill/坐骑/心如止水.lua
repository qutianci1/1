local 法术 = {
    类别 = '坐骑',
    类型 = 2,
    对象 = 0,
    条件 = 0,
    名称 = '心如止水',
    种族 = 1
}

function 法术:计算(坐骑, 召唤)
    local hp, kbhs = self:效果计算(坐骑)
    local jhp = math.floor(召唤.最大气血 * hp * 0.01)
    召唤.最大气血 = 召唤.最大气血 + jhp
    召唤.气血 = 召唤.气血 + jhp
    召唤.抗性.抗封印 = 召唤.抗性.抗封印 + kbhs
    召唤.抗性.抗混乱 = 召唤.抗性.抗混乱 + kbhs
    召唤.抗性.抗昏睡 = 召唤.抗性.抗昏睡 + kbhs
    召唤.抗性.抗遗忘 = 召唤.抗性.抗遗忘 + kbhs
end

