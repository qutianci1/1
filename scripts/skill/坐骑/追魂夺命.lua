local 法术 = {
    类别 = '坐骑',
    类型 = 2,
    对象 = 0,
    条件 = 0,
    名称 = '追魂夺命',
    种族 = 1
}

function 法术:计算(坐骑, 召唤)
    local sp, mlz = self:效果计算(坐骑)
    召唤.速度=召唤.速度+math.floor( 召唤.速度*sp*0.01 )
    召唤.抗性.命中率=召唤.抗性.命中率+mlz
    召唤.抗性.致命几率=召唤.抗性.致命几率+mlz
    召唤.抗性.连击率=召唤.抗性.连击率+mlz
end

