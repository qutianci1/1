        [20]={[1]=4.8,[2]=1.333333333},
        [21]={[1]=3.6,[2]=1},
        [22]={[1]=14.4,[2]=4},
        [23]={[1]=7.2,[2]=2}}
    local 坐骑系数={[1]=0.3,[2]=0.3,[3]=0.7,[4]=0.7,[5]=0,[6]=0,[7]=10000,[8]=10000,[9]=1.2}
    local 等级=坐骑.等级
    local 熟练=self.熟练度
    local 根骨=坐骑.根骨
    local 灵性=坐骑.灵性
    local 力量=坐骑.力量
    local 成长=1

    local sp = (根骨 * 坐骑系数[5] + 灵性 * 坐骑系数[1] + 力量 * 坐骑系数[4]) * 成长 / 坐骑参数[22][1] + 熟练 / 坐骑系数[7] / 坐骑参数[22][2]
